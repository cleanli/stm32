void Gpio_Init(void)
{
	GPIO_InitTypeDef iic_pin1, iic_pin2;

	RCC_APB2PeriphClockCmd(Port_Clock,ENABLE);
	iic_pin1.GPIO_Pin=(CD|RST|SCK|SDA);
	iic_pin1.GPIO_Mode=GPIO_Mode_Out_PP;
	iic_pin1.GPIO_Speed=GPIO_Speed_50MHz;
	GPIO_Init(GPIOB,&iic_pin1);
	iic_pin2.GPIO_Pin=CS;
	iic_pin2.GPIO_Mode=GPIO_Mode_Out_PP;
	iic_pin2.GPIO_Speed=GPIO_Speed_50MHz;
	GPIO_Init(GPIOA,&iic_pin2);
	CS_1();
	CD_1();
	RST_1();
	SCK_1();
	SDA_1();
}

