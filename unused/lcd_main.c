//send cmd/data to lcd
//C=0 data, C=1 cmd
void WriteData(unsigned char data, unsigned char C)
{
	unsigned char i,dat;
    CS_0();
	if(C==1)
	{
		CD_1();
	}
	else
	{
		CD_0();
 	}
	dat=data;
	for(i=0;i<8;i++)
	{
		if((dat&0x80)==0x80)
		{
			SDA_1();
		}
		else
		{
			SDA_0();
		}
		SCK_0();
		Delay(5);
		
		SCK_1();
		Delay(5);
		
		dat<<=1;
	}
	CS_1();	
}

void lcd_clr()
{
    unsigned char i,j;
    for(i=0;i<8;i++)//8 pages data
    {
        WriteData(0x40,0);//set line of rotating
        WriteData(0xb0|i,0);//set addr of page

        WriteData(0x10,0);//set addr of column(double bytes cmd)
        WriteData(0x00,0);//set column addr is 0

        for(j=0;j<192;j++)//192 columns
        {
            WriteData(0,1);
        }
    }
}

void Dispgraphic(unsigned char *p)
{
    unsigned char i,j;
    for(i=0;i<8;i++)//8 pages data
    {
        WriteData(0x40,0);//set line of rotating
        WriteData(0xb0|i,0);//set addr of page

        WriteData(0x10,0);//set addr of column(double bytes cmd)
        WriteData(0x00,0);//set column addr is 0

        for(j=0;j<192;j++)//192 columns
        {
            WriteData(p[i*192+j],1);
        }
    }
}

void lcd_write_byte(uint8_t page, uint8_t clm, uint8_t d)
{
    WriteData(0xb0+page,0);//set page addr

    WriteData(0x10+(clm>>4&0x0F),0);//set column addr(double bytes cmd)
    WriteData(clm&0x0F,0);
    WriteData(d,1);
}

/* Right Rotate 90
 *
 * 1000 0000       1010 0001
 * 0000 0000       0000 0000
 * 0000 0000       1000 0000
 * 0000 0000       0000 0000
 * 0000 0000  ---> 0000 0000
 * 0000 0000       0000 0000
 * 1000 0000       1000 0000
 * 0000 0000       0000 0000
 * 1010 0100       0000 0000
 */
void bytes_vertical_transform(uint8_t * in, uint8_t* out)
{
    uint32_t mask = 1<<7;
    for(int i=0;i<8;i++){
        out[i] = 0;
        for(int j=0;j<8;j++){
            out[i] = (out[i]<<1) + ((in[7-j]&mask)?1:0);
        }
        mask>>=1;
    }
}

